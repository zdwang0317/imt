package wzd.pageModel.pi;

public class Tpn {
	
	private String tpn;
	private int tpnOrder;
	private String status;
	private String ruleTypeId;
	private String remLayer;
	public String getTpn() {
		return tpn;
	}
	public void setTpn(String tpn) {
		this.tpn = tpn;
	}
	public int getTpnOrder() {
		return tpnOrder;
	}
	public void setTpnOrder(int tpnOrder) {
		this.tpnOrder = tpnOrder;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getRuleTypeId() {
		return ruleTypeId;
	}
	public void setRuleTypeId(String ruleTypeId) {
		this.ruleTypeId = ruleTypeId;
	}
	public String getRemLayer() {
		return remLayer;
	}
	public void setRemLayer(String remLayer) {
		this.remLayer = remLayer;
	}
	
}
